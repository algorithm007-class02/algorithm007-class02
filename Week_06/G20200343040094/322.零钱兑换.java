// @lc code=start

// 1.递归
// 2.DP
//     a.分治 minStep(amount) = MIN(minStep(amount), minStep(amount-coin)+1)
//     b.状态定义 f[i] 凑成金额i所需要的最少硬币数
//     c.DP方程 dp[i] = min(dp[i], dp[i-coin]+1)

class Solution {
    public int coinChange(int[] coins, int amount) {
        if (coins.length == 0)
            return -1;
        int[] dp = new int[amount + 1];
        Arrays.fill(dp, amount + 1);
        dp[0] = 0;
        for (int i = 1; i <= amount; ++i) {
            for (int coin : coins) {
                if (i >= coin) {
                    dp[i] = Math.min(dp[i], dp[i - coin] + 1);
                }
            }
        }
        return dp[amount] > amount ? -1 : dp[amount];
    }
}
